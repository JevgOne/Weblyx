// Helper function to get score label
export function getScoreLabel(score: number, lang: PDFLocale): string {
  const labels = SCORE_LABELS[lang];
  if (score <= 30) return labels.critical;
  if (score <= 50) return labels.belowAverage;
  if (score <= 70) return labels.average;
  if (score <= 85) return labels.good;
  return labels.excellent;
}

// Helper function to format text with variables
export function formatText(template: string, vars: Record<string, string | number>): string {
  let result = template;
  for (const [key, value] of Object.entries(vars)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value));
  }
  return result;
}
